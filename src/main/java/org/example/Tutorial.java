package org.example;

public class Tutorial {
    public static void main(String [] args) {
        // Tipos primitivos: inteiros - reais - lógicos - textuais
        // Strings guardam referências a objetos, diferente do char, que é um tipo primitivo (valor)
        int valor01;
        float valor02;
        boolean valor03;
        char letra;
        valor01 = 12;
        valor02 = 12.85f;
        valor03 = true;
        letra = 'A';
        System.out.println(valor01);
        System.out.println(valor02);
        System.out.println(valor03);
        System.out.println(letra);
        System.out.println("Parabéns por ter chegado até aqui!");
    }
}
